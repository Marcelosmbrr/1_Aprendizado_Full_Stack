<?php

    //While //Enquanto [condição], faça
    $limite = 5;
    $contador = 0;
    while($contador < $limite){ 

        echo $contador++;
        echo "<br>";

    }

    //Do While //Faça, enquanto [condição]..
    $limite = 5;
    $contador = 0;
    do{

        echo $contador++;
        echo "<br>";

    }while($contador < $limite);