//OUTROS MÉTODOS IMPORTANTES DO OBJETO ARRAY*************************/

const nomes = ["Fulano", "Beltrano", "Ciclano", "Mariliano"];

nomes.pop(); //Retirar o último elemento
console.log(nomes);

nomes.shift(); //Retirar o primeiro elemento
console.log(nomes);

nomes.push("Batman"); //Adiciona na última posição
console.log(nomes);

nomes.unshift("Mario"); //Adiciona na primeira posição
console.log(nomes);
