==== O QUE É HTACCESS ====

- O seu nome vem de HyperText access 
- A linguagem de programação utilizada no arquivo é uma própria para ele mesmo, mas sua forma visual é de texto puro
- O arquivo .htaccess serve para controlar diversos parâmetros do servidor Apache
- Ele é um arquivo de configuração de servidor distribuído, ou seja, ele configura o servidor apenas no diretório em que o arquivo .htaccess está
- Em uma arquitetura MVC, normalmente existe na pasta public

==== O QUE É POSSÍVEL FAZER COM ELE? ====

- Com ele, pode-se controlar diversos parâmetros, tais como: 

* Definir a página de erro padrão
* Definir página padrão de acesso ao site
* Acesso com e sem o “www"
* Bloqueio de acesso a pastas
* Controlar acesso por IP
* Controlar redirecionamentos
* Criar URLs amigáveis
* Redirecionar HTTP para HTTPS
* Definir limite de memória do servidor
* Configurar os processos de upload

==== QUANDO UTILIZAR O .HTACCESS ====

- Por controlar uma série de parâmetros de acesso, pode ser usado para melhorar o desempenho e utilização do site
- Por realizar essas mesmas coisas, pode ser usado para melhorar a segurança do site
- A somatória desses poderes do .htaccess fazem dele um ótimo mecanismo para melhorar o SEO do site

